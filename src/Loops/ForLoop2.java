package Loops;

public class ForLoop2 {
    public static void main(String[] args) {
        /*
        find sum of the numbers between 0 and 5 using for loop and show the sum at the end
        0+1+2+3+4+5=15
         */
        int total=0;
        for(int num =0;num<=5;num++){
           total+=num;

        } System.out.println(total);
    }
}
